public class Grade {
    private String grade;
    private String date;

    public Grade(String grade, String date) {
        this.grade = grade;
        this.date = date;
    }

    public String getGrade() {
        return grade;
    }

    public void setGrade(String grade) {
        this.grade = grade;
    }

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }
}
